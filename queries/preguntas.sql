    enunciado VARCHAR(255) NOT NULL, -- Texto de la pregunta
    audio_url VARCHAR(255),          -- URL del audio asociado a la pregunta
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

